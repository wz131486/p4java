package com.wz.se.ch02;

public class OperatorDemo04
{
	public static void main(String[] args)
	{
		int a = 10;
		int b = 20;

		System.out.println(a > b) ; // false
		System.out.println(a < b) ; // true
		System.out.println(a >= b) ; // false
		System.out.println(a <= b) ; // true
		System.out.println(a != b) ; // true
		System.out.println(a == b) ; // false

	}
}